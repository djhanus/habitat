        <h1>Our Mission</h1>
        <div class="text">
          <p>At Habitat for Humanity of Monroe County we build strength, stability, self-reliance and shelter--the only thing we give away is an opportunity. Our mission is to eliminate poverty housing by building decent, affordable homes in partnership with qualifying families.</p>

          <p>Our work in Monroe County has a special urgency as 1 in 5 people live in poverty. Monroe County is the least affordable place to live in the state of Indiana—an employee earning minimum wage must work 94 hours per week every week of the year to afford a two-bedroom apartment.</p>

          <p>All who qualify for the Habitat program do so based on the same three criteria: need for housing, ability to pay, willingness to partner. Each adult member living in a Habitat home must put in 250 hours of volunteering, called sweat equity prior to closing on their house.</p>

          <p>Through volunteer labor and tax-deductible donations of money and materials, our houses are sold to future homeowners at no profit and financed with affordable loans. As our partner families pay back their interest-free mortgage, we are able to offer future families an opportunity for home ownership making an impact for additional families.</p>
        </div>
