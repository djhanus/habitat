        <h1>History</h1>
        <div class="text">
          <p>Habitat for Humanity of Monroe County was founded in 1988 and operated entirely by volunteers until 1997. Currently, our affiliate has more than 10 full-time staff and has housed nearly 700 people--including almost 400 children. Currently, we are building an average of 12 homes each year and have on average 21 families in our program. Through the work of thousands of volunteers and community partners, Habitat for Humanity of Monroe County has built nearly 200 homes.</p>

          <p>Construction for the our first neighborhood, Cedar Chase, began in 2008 on Bloomington’s southwest side and is home to more than 30 families. The second neighborhood project off the B-Line trail, Trail View, began in 2014.</p>

          <p>Founded in 1976 by Millard and Linda Fuller, Habitat for Humanity is a global nonprofit housing organization working in local communities across all 50 states in the U.S. and in approximately 70 countries. Habitat’s vision is of a world where everyone has a decent place to live.</p>

          <p>Habitat invites people from all faiths and walks of life to work together in partnership, building houses with families in need. Discover more about the history of Habitat for Humanity online.</p>

          <p>Habitat for Humanity of Monroe County is an equal opportunity housing provider and adheres to Habitat for Humanity International’s non-proselytizing policy.</p>
